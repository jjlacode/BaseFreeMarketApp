package jjlacode.com.freelanceproject.model;

public class AgendaPresup {

    private String id_presupuesto;

    private String presupuesto;

    private String descripcion;

    private String cliente;

    private String estado;

    private String rutaFoto;

    private String peso;

    private String retraso;

    private String fechaEntrada;

    private String fechaEntregaPresup;

    private String fechaAcordada;

    private String fechaCalculada;

    private String fechaFinal;

    private String tipoEstado;

    public AgendaPresup() {
    }

    public String getId_presupuesto() {
        return id_presupuesto;
    }

    public void setId_presupuesto(String id_presupuesto) {
        this.id_presupuesto = id_presupuesto;
    }

    public String getPresupuesto() {
        return presupuesto;
    }

    public void setPresupuesto(String presupuesto) {
        this.presupuesto = presupuesto;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getRutaFoto() {
        return rutaFoto;
    }

    public void setRutaFoto(String rutaFoto) {
        this.rutaFoto = rutaFoto;
    }

    public String getPeso() {
        return peso;
    }

    public void setPeso(String peso) {
        this.peso = peso;
    }

    public String getRetraso() {
        return retraso;
    }

    public void setRetraso(String retraso) {
        this.retraso = retraso;
    }

    public String getFechaEntrada() {
        return fechaEntrada;
    }

    public void setFechaEntrada(String fechaEntrada) {
        this.fechaEntrada = fechaEntrada;
    }

    public String getFechaEntregaPresup() {
        return fechaEntregaPresup;
    }

    public void setFechaEntregaPresup(String fechaEntregaPresup) {
        this.fechaEntregaPresup = fechaEntregaPresup;
    }

    public String getFechaAcordada() {
        return fechaAcordada;
    }

    public void setFechaAcordada(String fechaAcordada) {
        this.fechaAcordada = fechaAcordada;
    }

    public String getFechaCalculada() {
        return fechaCalculada;
    }

    public void setFechaCalculada(String fechaCalculada) {
        this.fechaCalculada = fechaCalculada;
    }

    public String getFechaFinal() {
        return fechaFinal;
    }

    public void setFechaFinal(String fechaFinal) {
        this.fechaFinal = fechaFinal;
    }

    public String getTipoEstado() {
        return tipoEstado;
    }

    public void setTipoEstado(String tipoEstado) {
        this.tipoEstado = tipoEstado;
    }
}
